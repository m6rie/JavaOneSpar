package Variables;

public class Variables101 {
    public static void main(String[] args) {
        int a; // declaration of the variable
        a = 3456; // assignment of the variable
        double b = 54.9; // declaration and initialization
        char c = 'f';

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        c = 'r';
        System.out.println(c);
        System.out.println(c = 't'); // we can reassign the variable
    }
}
