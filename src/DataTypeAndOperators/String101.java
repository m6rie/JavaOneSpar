package DataTypeAndOperators;

public class String101 {
    public static void main(String[] args) {
        String newString = "It is raining flowers";
        System.out.println("Java resourceful. \nLet's master it!");
        System.out.println(newString.length()); // to get the length of the string
        System.out.println(newString.charAt(3)); // to get the char at the index position 3
        System.out.println(newString.substring(5,12)); // get the substring that starts at index 5 and finishes at 12
    }
}
